#!/bin/sh
# Persist and re-apply a stable STA MAC for ath11k.
#
# ath11k QCA6390 has no EEPROM MAC; the driver invents a new "permanent"
# address each module load. ConnMan stores favorites as wifi_<mac>_..., so
# a changing MAC forces re-entry of the Wi‑Fi password after every reboot.
#
# Usage:
#   persist-wlan-macaddr.sh prepare  — write .link MAC before modprobe
#   persist-wlan-macaddr.sh          — apply/save after iface exists + cleanup

PERSIST_DIR=/persist/wifi
PERSIST_FILE=$PERSIST_DIR/.macaddr
LINK_FILE=/run/systemd/network/10-wlp-mac.link
CONNMAN_DIR=/home/defaultuser/.local/share/system/privileged/connman

wait_iface() {
	iface=""
	timeout="${1:-20}"
	while [ "$timeout" -gt 0 ]; do
		for c in /sys/class/net/wlan0 /sys/class/net/wl*; do
			[ -e "$c" ] || continue
			iface=$(basename "$c")
			break
		done
		[ -n "$iface" ] && break
		sleep 1
		timeout=$((timeout - 1))
	done
	echo "$iface"
}

normalize_mac() {
	echo "$1" | tr 'A-F' 'a-f' | tr -d '\n\r '
}

mac_to_connman() {
	# 00:03:7f:12:78:78 -> 00037f127878
	echo "$1" | tr -d ':'
}

write_link_mac() {
	want=$1
	mkdir -p /run/systemd/network
	cat > "$LINK_FILE" <<EOF
# Generated by persist-wlan-macaddr.sh — do not edit
[Match]
Driver=ath11k_pci

[Link]
MACAddress=$want
MACAddressPolicy=none
NamePolicy=keep kernel database onboard slot path
EOF
	# Pick up /run link files for the next netdev creation
	udevadm control --reload >/dev/null 2>&1 || true
	echo "Prepared systemd .link MAC $want"
}

cleanup_stale_profiles() {
	want=$1
	prefix="wifi_$(mac_to_connman "$want")_"
	[ -d "$CONNMAN_DIR" ] || return 0
	for d in "$CONNMAN_DIR"/wifi_*; do
		[ -d "$d" ] || continue
		base=$(basename "$d")
		case "$base" in
		"$prefix"*) ;;
		*)
			echo "Removing stale ConnMan profile $base"
			rm -rf "$d"
			;;
		esac
	done
}

refresh_connman_wifi() {
	# Sailfish often has no connmanctl in PATH; use D-Bus instead.
	if command -v connmanctl >/dev/null 2>&1; then
		connmanctl disable wifi >/dev/null 2>&1 || true
		sleep 1
		connmanctl enable wifi >/dev/null 2>&1 || true
		return 0
	fi
	busctl call net.connman /net/connman/technology/wifi \
		net.connman.Technology SetProperty sv Powered b false >/dev/null 2>&1 || true
	sleep 1
	busctl call net.connman /net/connman/technology/wifi \
		net.connman.Technology SetProperty sv Powered b true >/dev/null 2>&1 || true
}

# --- prepare: before modprobe ---
if [ "${1:-}" = "prepare" ]; then
	if [ ! -e "$PERSIST_FILE" ]; then
		echo "No persisted WiFi MAC yet; skipping prepare"
		exit 0
	fi
	want=$(normalize_mac "$(cat "$PERSIST_FILE")")
	if [ -z "$want" ] || [ "${#want}" -lt 17 ]; then
		echo "Invalid persisted WiFi MAC in $PERSIST_FILE"
		exit 0
	fi
	write_link_mac "$want"
	exit 0
fi

# --- apply / first-boot save: after iface exists ---
if [ -e "$PERSIST_FILE" ]; then
	want=$(normalize_mac "$(cat "$PERSIST_FILE")")
	if [ -z "$want" ] || [ "${#want}" -lt 17 ]; then
		echo "Invalid persisted WiFi MAC in $PERSIST_FILE"
		exit 0
	fi

	iface=$(wait_iface 25)
	if [ -z "$iface" ]; then
		echo "Could not apply persisted WiFi MAC: interface not ready"
		exit 0
	fi

	cur=$(normalize_mac "$(cat /sys/class/net/$iface/address)")
	changed=0
	if [ "$cur" != "$want" ]; then
		echo "Applying persisted WiFi MAC $want on $iface (was $cur)"
		ip link set dev "$iface" down 2>/dev/null || true
		ip link set dev "$iface" address "$want" || {
			echo "Failed to set WiFi MAC on $iface"
			ip link set dev "$iface" up 2>/dev/null || true
			exit 0
		}
		ip link set dev "$iface" up 2>/dev/null || true
		changed=1
	else
		echo "WiFi MAC already $want on $iface"
	fi

	# Only bounce WiFi when the address actually changed; otherwise ConnMan
	# may rewrite stale wifi_<oldmac>_... profiles from memory.
	if [ "$changed" -eq 1 ]; then
		refresh_connman_wifi
		sleep 2
	fi
	cleanup_stale_profiles "$want"
	exit 0
fi

# First boot — interface must already exist (call after modprobe).
iface=$(wait_iface 15)
if [ -z "$iface" ]; then
	echo "Could not persist WiFi MAC: interface not available"
	exit 0
fi

mkdir -p "$PERSIST_DIR"
chmod 755 "$PERSIST_DIR"
wifi_mac=$(normalize_mac "$(cat /sys/class/net/$iface/address)")
echo -n "$wifi_mac" > "$PERSIST_FILE"
chmod 644 "$PERSIST_FILE"
write_link_mac "$wifi_mac"
echo "Persisted WiFi MAC $wifi_mac from $iface"
